CREATE TABLE public.user_role (
    user_id UUID NOT NULL REFERENCES public.profile(user_id) ON DELETE CASCADE,
    role_id BIGINT NOT NULL REFERENCES public.role(id) ON DELETE CASCADE,
    PRIMARY KEY (user_id, role_id)
);


DO $$
