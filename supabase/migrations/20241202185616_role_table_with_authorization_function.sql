    PERFORM public.add_audit_columns('public', 'user_role');
END $$;


CREATE FUNCTION public.has_role(
    role_name TEXT
) RETURNS BOOLEAN AS
$$
BEGIN 
    RETURN EXISTS (
        SELECT 1
        FROM user_role
        INNER JOIN role ON role.id = user_role.role_id
        WHERE user_role.user_id = (SELECT auth.uid())
        AND role.name = role_name
    );
END;
$$ LANGUAGE PLPGSQL;


